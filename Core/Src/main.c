void HAL_GPIO_EXTI_Callback( uint16_t GPIO_Pin)
{
	if(GPIO_Pin == GPIO_PIN_0){
		timestamp = TIM2->CNT;
		if (timestamp > 30) { //if the reset code is detected
			LED_NUMBER = cnt / 24;
			cnt = 0;
			captured_flag = 1;
		}
		else{ // if data is still streaming
			pattern[cnt] = (GPIOB->IDR & 0x01);
			cnt = cnt + 1;
		}
		TIM2->CNT = 0;

	}
}

